var b2Icon = document.getElementById("b2Icon");
if (b2Icon) {
  b2Icon.addEventListener("click", function () {
    var anchor = document.querySelector("[data-scroll-to='rectangleImage6']");
    if (anchor) {
      anchor.scrollIntoView({ block: "start", behavior: "smooth" });
    }
  });
}

